#문자열을 변수에 저장하는 방법
#세 명령문장 모두 수행하는 기능은 같다.
hello = 'Hello, World!'     #방법 1
hello = "Hello, World!"     #방법 2
hello = '''Hello, World!''' #방법 3


#여러 줄로 된 문자열 사용하는 방법
#여러 줄을 처리하는 문자열은 '''로 묶어서 사용한다.
hello = ''' Hello, World!
Python Study'''

#문자열 안에 작은따옴표나 큰따옴표 포함하는 방법
#"" 로 묶인 문장에 ''를 사용하거나,
#''로 묶인 문장에 ""를 사용한다.
s = "Python isn't difficult"
s = 'He said "Python is easy"'
